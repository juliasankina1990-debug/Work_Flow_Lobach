# ==================== ГЕНЕРАЦИЯ ====================

def generate_articles(count=100):
    """Генерирует и добавляет указанное количество статей."""
    with app.app_context():
        # Проверим, сколько уже статей
        existing = Item.query.count()
        print(f"Сейчас в базе {existing} статей. Добавляем {count} новых...")

        added = 0
        # Для разнообразия будем циклически проходить по темам
        topics_cycle = TOPICS * (count // len(TOPICS) + 1)

        for i, topic in enumerate(topics_cycle):
            if added >= count:
                break

            # Выбираем случайный заголовок из темы
            title_list = TITLES.get(topic['name'], [f"Статья про {topic['name']}"])
            title = random.choice(title_list) + f" (вариант {random.randint(1,5)})"

            # Выбираем автора
            author = random.choice(topic['authors'])

            # Выбираем 2-4 случайных тега из темы
            tags = random.sample(topic['tags'], min(4, len(topic['tags'])))

            # Генерируем дату (последние 3 месяца)
            days_ago = random.randint(0, 90)
            date = (datetime.now() - timedelta(days=days_ago)).strftime('%Y-%m-%d')

            # Превью (первое предложение контента)
            content_template = random.choice(CONTENT_TEMPLATES)
            content = content_template.format(title=title.lower())
            preview = content[:150] + "..." if len(content) > 150 else content

            # Создаём материал
            item = Item(
                title=title,
                type=topic['type'],
                type_icon=topic['type_icon'],
                tags=','.join(tags),
                date=date,
                department=topic['department'],
                department_owner=topic['department'],  # для простоты
                preview=preview,
                content=content,
                views=random.randint(0, 500),
                saved_count=random.randint(0, 50),
                author=author
            )
            db.session.add(item)
            added += 1

        db.session.commit()
        print(f"✅ Добавлено {added} статей. Теперь всего статей: {Item.query.count()}")

if __name__ == '__main__':
    generate_articles(100)  # Можно изменить число
